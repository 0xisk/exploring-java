    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        
        File Trainer = null;
        Scanner sc_Trainer = null;
        ArrayList<String> File_Data = new ArrayList<>();
        try {
            Trainer = new File("schedule.txt");
            sc_Trainer = new Scanner(Trainer);
            while (sc_Trainer.hasNext()) {                
                File_Data.add(sc_Trainer.nextLine());
            }
            
            String Mail = "";
            String Mail_Entered = Member_MAil.getText();
            
            for (int i = 0; i < File_Data.size(); i++) {
                String[] Line = File_Data.get(i).split("%");
                Mail = Line[0];
                if (Mail.equals(Mail_Entered)) {
                    found = true;
                }
            }
            
            if (found) {
                JOptionPane.showMessageDialog(null, "Email Found Successfuly", "Info", JOptionPane.INFORMATION_MESSAGE);
            }
            else{
                JOptionPane.showMessageDialog(null, "Email Not Found", "Error", JOptionPane.ERROR_MESSAGE);
            }
            
        } catch (FileNotFoundException ex) {
            Logger.getLogger(Coaches_Evaluation.class.getName()).log(Level.SEVERE, null, ex);
        }
        
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        if (found) {
            File Evaluatioin_File = new File("schedule.txt");
            Scanner sc_Evaluation = null;
            ArrayList<String> File_Evaluation_Data = new ArrayList<>();
            try {
                sc_Evaluation = new Scanner(Evaluatioin_File);
                
                while (sc_Evaluation.hasNext()) {                    
                    File_Evaluation_Data.add(sc_Evaluation.nextLine());
                }
                String Mail;
                Mail = Member_MAil.getText();
                for (int i = 0; i < File_Evaluation_Data.size(); i++) {
                    String Line1 = File_Evaluation_Data.get(i);
                    String[] Line2 = Line1.split("%");
                    if (Line2[0].equals(Mail)) {
                        File_Evaluation_Data.set(i, Line1+"%"+Evaluation.getText());
                    }
                }
                
                PrintWriter pw = new PrintWriter("schedule.txt");
                for (int i = 0; i < File_Evaluation_Data.size(); i++) {
                    pw.println(File_Evaluation_Data.get(i));
                }
                pw.close();
                
            } catch (FileNotFoundException ex) {
                JOptionPane.showMessageDialog(null, "File schedule.txt is Not Found", "Error", JOptionPane.ERROR_MESSAGE);
            }
            
        }
        else
            JOptionPane.showMessageDialog(null, "You Can't Evaluate UnExisted Member", "Error", JOptionPane.ERROR_MESSAGE);
    }//GEN-LAST:event_jButton2ActionPerformed

